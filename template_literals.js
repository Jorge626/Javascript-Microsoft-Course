// Use placeholders ${} for variables or expressions
// Respect line breaks, doesn't need newline character "\n"
// Template literals only require the backtick character ``, at beginning and end of string
let str1 = "Javascript";
let str2 = "fun";

console.log(`I am writing code in ${str1}`);
console.log(`Formatting in ${str1} is ${str2}!`);

let bool1 = true;
console.log(`1 + 1 is ${1 + 1}`);
console.log(`The opposite of true is ${!bool1}`);